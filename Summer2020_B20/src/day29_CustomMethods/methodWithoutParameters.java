package day29_CustomMethods;

public class methodWithoutParameters {
    public static void main(String[] args) {

        for(int i = 10; i>=6; i--){
            System.out.println("Hello");
        }
        System.out.println("Cybertek");

        for(int i=10; i>=6; i--){
            System.out.println("Hello");
        }
        System.out.println("Irene");

        for(int i=10; i>=6; i--){
            System.out.println("Hello");

        }

    }
}
