package day21_Loops;

public class DoWhileLoopPractice {
    public static void main(String[] args) {
        int num = 1;
        do{
            System.out.print(num+" ");
            num++;
        }while(num<20);
        System.out.println(num);

        char ch = 'Z';
        do {
            System.out.println(ch + " ");
            ch--;
        }while(ch>='A');
        System.out.print(ch);
        }

    }

